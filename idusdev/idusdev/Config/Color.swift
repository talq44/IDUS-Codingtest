import Foundation
import UIKit

extension UIColor{
    class var back_gray_color:UIColor{
        return UIColor.init(red: 242/255, green: 242/255, blue: 242/255, alpha: 1)
    }
    class var line_gray_color:UIColor {
        return UIColor.init(red: 221/255, green: 221/255, blue: 221/255, alpha: 1)
    }
}

